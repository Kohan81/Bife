public class UsingVariables2 {
    public static void main(String[] args) {

        char letter = 'A';
        int number1 = 25;
        double number2 = 35.67;

        // Values stored in variables can be output to screen
        System.out.println("Value stored in letter is " + letter);
        System.out.println("Value stored in number1 is " + number1);
        System.out.println("Value stored in number2 is " + number2);
        System.out.println();

        // Values of the variables can be changed
        letter = 'B';
        number1 = 157;
        number2 = -157.69;

        // Values stored in variables output to screen
        System.out.println("Value now in letter is " + letter);
        System.out.println("Value now in number1 is " + number1);
        System.out.println("Value now in number2 is " + number2);
        System.out.println();

    }//main

}//class
